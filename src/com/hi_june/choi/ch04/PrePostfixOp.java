package com.hi_june.choi.ch04;

public class PrePostfixOp {
    public static void main(String[] args) {
        int num1 = 7;
        System.out.println(++num1);
        System.out.println(++num1);
        System.out.println(num1);
        System.out.println();

        int num2 = 7;
        System.out.println(num2++);
        System.out.println(num2++);
        System.out.println(num2);
    }
}
